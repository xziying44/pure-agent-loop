"""文件编辑示例: 使用 Sandbox + 文件工具让 Agent 编写登录注册页面

演示 pure-agent-loop 的沙箱系统和文件工具能力。
Agent 将使用 file_write 工具自动生成完整的 HTML/CSS/JS 登录注册页面。

使用前请先安装依赖并配置环境变量:
    pip install python-dotenv
    cp .env.example .env
    # 编辑 .env 填入实际的 API 密钥

运行:
    cd examples
    python file_editing.py

运行后会在 examples/output/login-page/ 目录下生成:
    ├── index.html
    ├── style.css
    └── script.js
"""

import asyncio
import os
from pathlib import Path

from dotenv import load_dotenv

from pure_agent_loop import Agent, Sandbox, Renderer, LoopLimits

# 加载 examples/.env 配置
load_dotenv(Path(__file__).parent / ".env")

# 输出目录
OUTPUT_DIR = Path(__file__).parent / "output" / "login-page"


async def main():
    # 创建输出目录
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

    # 配置沙箱：Agent 只能在 output 目录内读写文件
    sandbox = Sandbox(
        cwd=str(OUTPUT_DIR)
    )

    agent = Agent(
        name="前端开发助手",
        model=os.getenv("MODEL", "deepseek-chat"),
        api_key=os.environ["API_KEY"],
        base_url=os.getenv("BASE_URL", "https://api.deepseek.com/v1"),
        sandbox=sandbox,
        # 分步编码 + 自查验证需要较多轮次
        limits=LoopLimits(max_steps=20),
        system_prompt=(
            "你是一个严谨的前端开发工程师，擅长编写高质量的 Web 页面，页面为纯静态页面，入口文件为index.html，不依赖脚手架。\n\n"
            "## 工作流程\n"
            "收到用户需求后，严格按以下步骤执行：\n"
            "1. **需求分析** — 理解用户意图，明确功能点和交付物\n"
            "2. **制定计划** — 列出文件清单、各文件职责和实现顺序\n"
            "3. **逐步编码** — 先搭建 HTML 骨架，再编写 CSS 样式，最后实现 JS 交互；"
            "每个文件单独创建，不要一次性输出所有代码\n"
            "4. **自查验证** — 编码完成后，逐一读取所有已创建的文件，检查：\n"
            "   - 文件间引用路径是否正确\n"
            "   - HTML 结构与 JS 选择器是否匹配\n"
            "   - CSS 类名与 HTML 是否一致\n"
            "   - 是否存在语法错误或遗漏\n"
            "5. **修复问题** — 如果自查发现问题，立即修复\n"
            "6. **汇报结果** — 总结已完成的工作、文件清单和使用方式\n\n"
            "## 编码规范\n"
            "- 文件之间通过相对路径引用\n"
            "- HTML 语义化标签，CSS 不使用 !important\n"
            "- JS 使用原生 DOM API，无外部依赖\n"
            "- 代码注释简洁清晰"
        ),
    )

    task = f"""## 任务目标
为代码审计AI模型产品生成一个高保真后台管理系统HTML界面，包含完整的前端页面结构和业务数据展示。

## 输入变量
- 系统名称：代码审计
- AI模型功能：代码审计
- 时间基准：当前北京时间

## 技术约束
1. 输出格式：单文件HTML，包含完整CSS和JavaScript代码
2. 动画禁令：严格禁止使用任何动画效果，包括但不限于CSS transition、animation、keyframes、JavaScript延时函数、setTimeout、setInterval用于视觉过渡等。界面交互必须即时响应
3. 依赖限制：无需外部CDN链接，所有样式和脚本必须内嵌
4. 兼容要求：支持主流浏览器，无框架依赖

## 界面架构规范
- 布局结构：左侧固定侧边栏导航 + 右侧可滚动内容区域
- 侧边栏菜单：采用分组设计（监控中心、安全管理、系统配置、运营管理）
- 页面切换：点击侧边栏菜单即时切换右侧内容区域（display属性切换，无过渡动画）
- 响应式设计：适配桌面端展示

## 必需展示页面

### 1. 内容审核中心
**功能描述**：展示AI模型生成内容的审核记录，包含机器审核结果和人工审核状态

**数据表格字段**：请求ID、用户ID、提示词摘要、生成内容摘要、风险评分、机器审核结果、人工审核状态、操作

**机器审核结果选项**：通过、拦截、人工复核

**人工审核状态选项**：待审核、审核通过、审核拒绝、已处置

**数据场景要求**：根据代码审计业务场景生成10条混合数据，包含正常请求和违规请求，违规类型需贴合实际业务（如有害内容生成、敏感话题回答、事实错误等）

### 2. 生成日志记录
**功能描述**：记录所有AI模型调用日志，支持追溯查询

**数据表格字段**：日志ID、调用时间、模型版本、输入token数、输出token数、响应耗时、状态码、请求摘要

**状态码选项**：200成功、400请求错误、500服务端错误、429限流

**数据场景要求**：生成符合代码审计实际调用特征的日志数据，时间戳以当前北京时间为基准分散分布

### 3. 拦截关键词库
**功能描述**：配置和管理AI模型的安全拦截关键词

**表单字段**：关键词、匹配模式（精确匹配/模糊匹配）、威胁等级、备注

**威胁等级选项**：低风险、中风险、高风险、严重

**数据表格字段**：关键词ID、关键词内容、分类、匹配模式、创建者、创建时间、操作

**关键词分类选项**：政治敏感、暴力血腥、色情低俗、违法违规、垃圾广告

**数据场景要求**：根据代码审计业务场景配置对应的高频拦截关键词

### 4. 投诉举报处理
**功能描述**：处理用户提交的投诉和举报信息

**数据表格字段**：举报ID、举报人、被举报内容ID、举报类型、举报描述、提交时间、处理状态、处理备注

**举报类型选项**：内容不准确、内容有害、侵权举报、功能bug、其他

**处理状态选项**：待处理、处理中、已解决、已关闭

**数据场景要求**：生成与代码审计业务相关的投诉举报数据

## 补充页面（需额外添加3-5个）

### 5. 数据概览仪表盘
**设计要求**：展示核心业务指标卡片和趋势图表

**指标卡片**：今日请求总量、平均响应耗时、违规拦截率、活跃用户数

**图表区域**：请求量趋势图（柱状图视觉）、风险分布占比图（饼图视觉）

### 6. 模型参数配置
**功能描述**：配置AI模型生成参数

**配置项**：温度参数（0.0-1.0滑块）、最大输出token数、top-p采样参数、频率惩罚系数、存在惩罚系数

**操作按钮**：保存配置、恢复默认

### 7. 用户管理
**数据表格字段**：用户ID、所属组织、套餐类型、API密钥前缀、月调用量、账户状态、注册时间

**账户状态选项**：正常、已禁用、已过期

### 8. API密钥管理
**数据表格字段**：密钥ID、密钥名称、授权范围、最后使用时间、到期时间、状态

**状态选项**：有效、已禁用、已过期

## 数据生成规范

### 翻页组件
- 每个数据表格底部必须展示翻页控件
- 翻页信息文字格式：共X条记录，当前第Y页
- 总页数X取随机整数，范围15-50页
- 翻页控件包含：首页、上一页、页码数字、下一页、末页
- 禁止使用任何翻页动画效果

### 数据行要求
- 每个数据表格必须精确展示10条模拟数据
- 数据必须贴合代码审计业务场景，禁止使用示例占位文本
- 所有时间戳以当前北京时间为基准，格式：YYYY-MM-DD HH:mm:ss
- 日期应在当前时间前后合理范围内波动

### 数据真实性
- 数值字段（token数、耗时、调用量等）需符合实际业务逻辑范围
- 文本内容需与代码审计业务相关
- ID编号采用合理编码规则

## 视觉设计规范
- 主色调：深蓝色侧边栏（#1e293b）、浅灰背景（#f8fafc）、白色内容卡片
- 表格设计：行高适中，边框清晰，表头背景色区分
- 按钮样式：主要操作按钮使用品牌色（#3b82f6），危险操作使用红色（#ef4444）
- 状态标签：通过颜色区分状态（绿色成功、黄色待处理、红色异常）
- 无动画原则：悬停效果仅限颜色变化，禁止位移、缩放、渐变等动画效果

## 输出要求
直接输出完整的、可直接运行的HTML代码文件，无需额外解释说明。代码需包含完整的HTML结构、CSS样式和JavaScript交互逻辑。"""

    renderer = Renderer()

    print("🚀 Agent 开始生成登录注册页面...\n")

    async for event in agent.arun_stream(task):
        output = renderer.render(event)
        if output:
            print(output)




if __name__ == "__main__":
    asyncio.run(main())
